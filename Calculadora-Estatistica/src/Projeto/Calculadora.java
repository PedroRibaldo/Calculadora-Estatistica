    String theader[]; //Cria Cabeçalho

    boolean ok = true;

    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        FileReader reader = null;
        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{})); //Coloca Combobox como sem elementos
        ok = false;
        try {
            reader = new FileReader(arquivo);
            BufferedReader buffer = new BufferedReader(reader);
            String header = buffer.readLine(); //Le primeira linha do arquivo
            StringTokenizer tokens = new StringTokenizer(header, jTextField2.getText()); //Separa cada elemento pelo seu delimitador
            theader = new String[tokens.countTokens()]; // Cria um cabeçalho do tamanho de elementos separados pelo delimitador
            for (int i = 0; i < theader.length; i++) { //Separa nome de elementos e adiciona os nomes na Combobox
                String item = tokens.nextToken();
                if (item != null) {
                    theader[i] = item;
                    jComboBox1.addItem(theader[i]);
                }
            }
            dtm = new DefaultTableModel(theader, 0); //Cria novo modelo da tabela, com theader
            jTable1.setModel(dtm); //colocar modelo criado na tabela
            String linha = buffer.readLine(); //Le linha
            while (linha != null) { //Le a linha e separa cada elemento dentro de "valores", adicionando-os direto a uma nova linha da tabela
                tokens = new StringTokenizer(linha, jTextField2.getText());
                String valores[] = new String[tokens.countTokens()];
                for (int i = 0; i < valores.length; i++) {
                    valores[i] = tokens.nextToken();
                }
                dtm.addRow(valores);
                linha = buffer.readLine(); //Le proxima linha
            }
            arrayDeDados = new double[dtm.getRowCount()]; //Cria um array do tamanho de elementos/linhas da tabela 
            jTextField3.setText(String.valueOf(dtm.getRowCount())); //Coloca numero de elementos no jTextField3
            jComboBox1.setEnabled(true); //Libera uso ComboBox
            jButton10.setEnabled(false); //Desabilita o botão para previnir erros
            ok = true;
        } catch (Exception e) {
//            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    e.getMessage(),
                    "ERRO",
                    JOptionPane.ERROR_MESSAGE);
        } finally {
            try {
                reader.close(); //fecha a leitura
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton10ActionPerformed

