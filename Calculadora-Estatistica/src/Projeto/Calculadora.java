    private XYDataset createXYDataset() {
        XYSeriesCollection dataset = new XYSeriesCollection();
        XYSeries serie = new XYSeries("Grafico");
//Inserir o conjunto de valores, um para cada linha da tabela. Ex.:
        int n = dtm.getRowCount();
        HashMap<Double, Integer> valores = Funcoes.carregarMapa(arrayDeDados);
        for (Map.Entry<Double, Integer> entry : valores.entrySet()) {
            serie.add(entry.getKey(),entry.getValue());
        }
//Adicionar a série ao dataset e retornar
        dataset.addSeries(serie);
        return dataset;
    }

